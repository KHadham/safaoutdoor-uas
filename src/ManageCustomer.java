    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        if (txt_nama_depan.getText().equals("") || txt_nama_belakang.getText().equals("") || txt_alamat.getText().equals("") || txt_kota.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Data harus di isi semua", "error", JOptionPane.INFORMATION_MESSAGE);
        } else {
            try {
                String sql = "insert into customers values (?,?,?,?,?,?)";
                PreparedStatement stat = konek.prepareStatement(sql);
                stat.setString(1, null);
                stat.setString(2, txt_nama_depan.getText());
                stat.setString(3, txt_nama_belakang.getText());
                stat.setString(4, txt_alamat.getText());
                stat.setString(5, txt_kota.getText());
                stat.setString(6, txt_telepon.getText());
                stat.executeUpdate();

                JOptionPane.showMessageDialog(null, "Berhasil tambah data", "success", JOptionPane.INFORMATION_MESSAGE);
                datatable();
            } catch (Exception e) {
                System.out.println("erorr: btn_addActionPerformed " + e);
            }
            Clear();
        }
    }//GEN-LAST:event_btn_addActionPerformed

