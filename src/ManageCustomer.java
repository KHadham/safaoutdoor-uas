    protected void datatable() {
        Object[] Baris = {"ID Pelanggan", "Nama depan", "Nama Belakang", "Alamat", "Kota", "No Telepon"};
        tabmode = new DefaultTableModel(null, Baris);
        try {
            String query = "SELECT * FROM customers";

            Statement stat = konek.createStatement();
            ResultSet hasil = stat.executeQuery(query);

            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    "PLG_" + hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                    hasil.getString(6),});
            }
            tabel.setModel(tabmode);
        } catch (Exception e) {
            System.out.println("erorr: " + e);
        }
    }

    protected void datatable2(String id) {
        Object[] Baris = {"ID Rental", "Nama Customer", "Tanggal Sewa", "Lama sewa", "Total Harga", "Status"};
        tabmode2 = new DefaultTableModel(null, Baris);
        try {

            String query = "SELECT\n"
                    + "    r.id_rental,\n"
                    + "    c.nama_depan,\n"
                    + "    c.nama_belakang,\n"
                    + "    r.created_at,\n"
                    + "    r.rental_days,\n"
                    + "    r.total_price,\n"
                    + "    r.status \n"
                    + "FROM\n"
                    + "    rentals r\n"
                    + "JOIN\n"
                    + "    customers c ON c.id_customer = r.id_customer\n"
                    + "WHERE r.id_customer = ?";

//            Statement stat = konek.createStatement();
//            ResultSet hasil = stat.executeQuery(query);
            PreparedStatement stat = konek.prepareStatement(query);

            stat.setString(1, id);

            ResultSet hasil = stat.executeQuery();

            while (hasil.next()) {
                tabmode2.addRow(new Object[]{
                    "RTL_" + hasil.getString(1),
                    hasil.getString(2) + " " + hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                    hasil.getString(6),
                    hasil.getString(7),});
            }

            tabel1.setModel(tabmode2);
        } catch (Exception e) {
            System.out.println("erorr: " + e);
        }
    }

